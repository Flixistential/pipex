#include "libftps.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ptr;
	size_t	i;

	i = 0;
	ptr = s;
	while (i < n)
	{
		*ptr = '\0';
		ptr++;
		i++;
	}
}
